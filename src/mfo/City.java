package mfo;

public class City {
	//each object of this class will store coordinates of one city
	
	double x;
	double y;

	public City() {
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
}
